def load(path):
    with open(path, encoding="utf-8") as f:
        return json.load(f)


def resolve_params(doc, op):
    out = []
    for p in op.get("parameters") or []:
        if "$ref" in p:
            name = p["$ref"].rsplit("/", 1)[-1]
            out.append(doc["parameters"][name])
        else:
            out.append(p)
    return out


def where(p):
    return {"path": "URL path", "query": "query string", "formData": "form field",
            "header": "header"}.get(p["in"], p["in"])


def first_sentence(text):
    text = (text or "").split(" Learn more: ")[0].strip()
    dot = text.find(". ")
    return text if dot < 0 else text[:dot + 1]


def schema_fields(doc, ref_name, depth=0):
    """Render the top-level fields of a definition as markdown bullets."""
    d = doc["definitions"].get(ref_name)
    if not d or "properties" not in d:
        return []
    lines = []
    for key, prop in d["properties"].items():
        if "$ref" in prop:
            target = prop["$ref"].rsplit("/", 1)[-1]
            lines.append(f"- `{key}` — {target.replace('Sdp', '')} object")
            continue
        typ = prop.get("type", "object")
        note = prop.get("description") or prop.get("x-ms-summary") or ""
        if typ == "array":
            item = prop.get("items", {})
            iref = item.get("$ref", "").rsplit("/", 1)[-1]
            sub = ""
            if iref and depth == 0:
                inner = doc["definitions"].get(iref, {}).get("properties", {})
                sub = " — items: " + ", ".join(f"`{k}`" for k in inner) if inner else ""
            lines.append(f"- `{key}` — array{sub}")
        else:
            lines.append(f"- `{key}` — {typ}" + (f" — {note}" if note else ""))
    return lines


def render_op(cname, doc, path, verb, op):
    info = doc["info"]
    opid = op["operationId"]
    c = CONTENT[cname].get(opid)
    if c is None:
        raise SystemExit(f"missing curated content: {cname}/{opid}")
    params = resolve_params(doc, op)
    endpoint = f"{verb.upper()} {doc.get('basePath', '')}{path}"
    v1 = cname == "sdp-cmdb-v1"

    md = [GENERATED_MARK, "", f"# {op['summary']}", ""]
    md.append(f"**Connector:** [{info['title']}](../README.md) (`{cname}`) · "
              f"**Action index:** [all actions](README.md)")
    md.append(f"**Endpoint:** `{endpoint}` · **Operation ID:** `{opid}`")
    md += ["", c["intro"], ""]

    # Parameters ------------------------------------------------------------
    md.append("## Parameters")
    md.append("")
    if params:
        md.append("| Designer label | Name | Where | Type | Required |")
        md.append("|---|---|---|---|---|")
        for p in params:
            label = p.get("x-ms-summary", p["name"])
            req = "yes" if p.get("required") else "no"
            md.append(f"| {label} | `{p['name']}` | {where(p)} | {p.get('type', 'string')} | {req} |")
        md.append("")
        for p in params:
            label = p.get("x-ms-summary", p["name"])
            md.append(f"### {label} (`{p['name']}`)")
            md.append("")
            if p["name"] == "Accept":
                md.append(ACCEPT_NOTE)
            elif p["name"] in c["params"]:
                md.append(c["params"][p["name"]])
            elif p["name"] in ("input_data", "INPUT_DATA"):
                md.append(first_sentence(p.get("description")) +
                          " Full format documented in the payload section below.")
            else:
                md.append(first_sentence(p.get("description")) or "See the table above.")
            md.append("")
    else:
        md.append("None — the URL (plus the connection's auth) is the entire call.")
        md.append("")

    # input_data deep dive ----------------------------------------------------
    if c["input_data"]:
        if not c["input_data"].lstrip().startswith("##"):
            md.append("## The `input_data` payload — formats and variations")
            md.append("")
        md.append(c["input_data"])
        md.append("")

    # Auth --------------------------------------------------------------------
    md.append(V1_AUTH if v1 else V3_AUTH)
    md.append("")

    # Response ------------------------------------------------------------------
    md.append("## Response")
    md.append("")
    ok_codes = [k for k in op.get("responses", {}) if k != "default"]
    ref = None
    for k in sorted(ok_codes):
        s = op["responses"][k].get("schema")
        if s and "$ref" in s:
            ref = s["$ref"].rsplit("/", 1)[-1]
            break
    if ref:
        md.append(f"Success ({'/'.join(sorted(ok_codes))}) returns the `{ref}` shape:")
        md.append("")
        md += schema_fields(doc, ref)
        md.append("")
    if c["response"]:
        md.append(c["response"])
        md.append("")
    if not v1:
        md.append(V3_RESPONSE_STATUS)
        md.append("")
    if v1:
        md.append("Anything else comes back on the `default` response — V1 failures put the "
                  "reason in `result.message`, and V3-style rejections can also appear.")
    else:
        md.append("Anything else comes back on the `default` response as the SDP error "
                  "envelope — surface `status_code` and `messages` in your flow's failure "
                  "branch.")
    md.append("")

    # Notes -----------------------------------------------------------------
    md.append("## Notes, limits & verification")
    md.append("")
    md.append(f"- {c['verified']}")
    for g in c["gotchas"]:
        md.append(f"- {g}")
    md.append("")

    # References --------------------------------------------------------------
    md.append("## References")
    md.append("")
    for label, url in c["refs"]:
        md.append(f"- [{label}]({url})")
    md.append(f"- [Connector README](../README.md) · [Repo README]({REPO}) · "
              f"[LESSONS.md](../../../LESSONS.md)")
    md.append("")
    return "\n".join(md)


def render_index(cname, doc, rows):
    info = doc["info"]
    md = [GENERATED_MARK, "", f"# {info['title']} — action docs", ""]
    md.append(f"One page per action of the [`{cname}`](../README.md) connector: every "
              "parameter explained, plus copy-paste `input_data` variations. These pages are "
              "the targets of each operation's **Learn more** link (`externalDocs` + the "
              "description suffix in the definition).")
    md += ["", "| Action | Operation ID | Endpoint | Doc |", "|---|---|---|---|"]
    for summary, opid, endpoint, fname in rows:
        md.append(f"| {summary} | `{opid}` | `{endpoint}` | [{fname}]({fname}) |")
    md.append("")
    return "\n".join(md)


def main():
    total = 0
    for cname in sorted(CONTENT):
        cdir = os.path.join(ROOT, "connectors", cname)
        doc = load(os.path.join(cdir, "apiDefinition.swagger.json"))
        ops = [(path, verb, op)
               for path, methods in doc["paths"].items()
               for verb, op in methods.items()]
        have = {op["operationId"] for _, _, op in ops}
        curated = set(CONTENT[cname])
        if have != curated:
            raise SystemExit(f"{cname}: content/spec drift — missing {sorted(have - curated)}, "
                             f"stale {sorted(curated - have)}")
        adir = os.path.join(cdir, "actions")
        os.makedirs(adir, exist_ok=True)
        rows = []
        for path, verb, op in ops:
            fname = kebab(op["operationId"]) + ".md"
            with open(os.path.join(adir, fname), "w", encoding="utf-8", newline="\n") as f:
                f.write(render_op(cname, doc, path, verb, op))
            rows.append((op["summary"], op["operationId"],
                         f"{verb.upper()} {doc.get('basePath', '')}{path}", fname))
            total += 1
        rows.sort(key=lambda r: r[3])
        with open(os.path.join(adir, "README.md"), "w", encoding="utf-8", newline="\n") as f:
            f.write(render_index(cname, doc, rows))
        print(f"{cname}: {len(rows)} action docs")
    print(f"total: {total} action docs")


if __name__ == "__main__":
    main()
