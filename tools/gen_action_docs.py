def list_info_section(default_json, module_examples, child_note=None):
    """The full list_info tutorial, with module-specific filter examples."""
    out = ["""## The `input_data` envelope — every variation

This action sends `input_data` as a **URL query parameter** holding a `list_info` JSON
envelope. The connector URL-encodes it for you — just paste JSON. Leave the box empty (or keep
the preset default) for SDP's defaults."""]
    if child_note:
        out.append(child_note)
    out += [JSON_RULES, LIST_INFO_KEYS, """\
### Variation 1 — plain paging

```json
{"list_info":{"row_count":25,"start_index":1}}
```

Page 2 is `"start_index":26`, and so on. Add `"get_total_count":true` on the first call to
size the loop:

```json
{"list_info":{"row_count":25,"start_index":1,"get_total_count":true}}
```

### Variation 2 — sorting

```json
{"list_info":{"row_count":25,"sort_field":"created_time","sort_order":"desc"}}
```
""", "### Variation 3 — filtering with `search_criteria`\n\n" + module_examples,
        CONDITIONS_TABLE, """\
### Variation 4 — multiple values (IN-style)

Use `values` (plural) with a list:

```json
{"list_info":{"row_count":50,"search_criteria":{"field":"priority.name","condition":"is","values":["High","Urgent"]}}}
```

### Variation 5 — combining criteria with AND / OR

`search_criteria` can be an **array**; every entry after the first carries a
`logical_operator` (`"AND"` or `"OR"`):

```json
{"list_info":{"search_criteria":[
  {"field":"status.name","condition":"is","value":"Open"},
  {"field":"priority.name","condition":"is","value":"High","logical_operator":"AND"}
]}}
```

### Variation 6 — grouped (nested) criteria with `children`

`children` nests sub-criteria under a parent, i.e. parentheses. This means
*status is Open AND (created after 2025-01-01)*:

```json
{"list_info":{"search_criteria":{
  "field":"status.name","condition":"is","value":"Open",
  "children":[{"field":"created_time","condition":"greater than","value":"1735689600000","logical_operator":"AND"}]
}}}
```

### Variation 7 — date windows

Dates in criteria are **epoch milliseconds as digit strings** (UTC). A window uses `between`
with both bounds in `values` — this is 2025-01-01 to 2025-07-01:

```json
{"list_info":{"search_criteria":{"field":"created_time","condition":"between","values":["1735689600000","1751328000000"]}}}
```

Power Automate tip for "now" in epoch ms:
`div(sub(ticks(utcNow()), ticks('1970-01-01T00:00:00Z')), 10000)`.

### Variation 8 — only the fields you need

```json
{"list_info":{"row_count":100,"fields_required":["id","subject","status"]}}
```
"""]
    if default_json:
        out.append("The parameter's preset default is:\n\n```json\n" + default_json + "\n```")
    return "\n\n".join(out)


